//Cars
const verticalCar = {
    position: { x: 14, y: 23 },
    shape: [
        [1, 1],
        [1, 1],
        [1, 1]
    ]
}
const horizontalCar = {
    position: { x: 7, y: 7 },
    shape: [
        [1, 1, 1],
        [1, 1, 1]
    ]

}


function draw() {
    context.fillStyle = '#808080'
    context.fillRect(0, 0, canvasSize.canvasWidth, canvasSize.canvasHeight)

    board.forEach((row, y) => {
        row.forEach((value, x) => {
            if (value === 1) {
                context.fillStyle = 'greenyellow'
                context.fillRect(x, y, 1, 1)
            }
        })
    })

    verticalCar.shape.forEach((row, y) => {
        row.forEach((value, x) => {
            if (value) {
                context.fillStyle = 'yellow'
                context.fillRect(x + verticalCar.position.x, y + verticalCar.position.y, 1, 1)
            }
        })
    })
    horizontalCar.shape.forEach((row, y) => {
        row.forEach((value, x) => {
            if (value) {
                context.fillStyle = 'yellow'
                context.fillRect(x + horizontalCar.position.x, y + horizontalCar.position.y, 1, 1)
            }
        })
    })
}

// 2. game loop
function update() {
    draw()
    requestAnimationFrame(update)
}





update()
